function C_P = CalculatePowerCoefficient(lambda, theta)

x = rad2deg(theta);
y = lambda;

% DS Should be also in the config file!
       p00 =     -0.6694;  %(-0.7044, -0.6343)
       p10 =     0.03285; % (0.03027, 0.03543)
       p01 =      0.3854;  %(0.3708, 0.4)
       p20 =  -0.0007651;  %(-0.0008281, -0.0007022)
       p11 =    -0.01064;  %(-0.01216, -0.009114)
       p02 =    -0.04491;  %(-0.04708, -0.04275)
       p21 =   0.0003777; % (0.0003447, 0.0004107)
       p12 =   0.0004907;  %(0.0001845, 0.0007969)
       p03 =    0.002206; %(0.00207, 0.002341)
       p22 =  -8.444e-05; % (-8.861e-05, -8.028e-05)
       p13 =   1.861e-05; %(-4.725e-07, 3.769e-05)
       p04 =  -4.366e-05;  %(-4.671e-05, -4.061e-05)


C_P = p00 + p10*x + p01*y + p20*x^2 + p11*x*y + p02*y^2 + p21*x^2*y + p12*x*y^2 + p03*y^3 + p22*x^2*y^2 + p13*x*y^3 + p04*y^4;

end