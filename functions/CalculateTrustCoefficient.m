function C_T = CalculateTrustCoefficient(lambda, theta)

x = rad2deg(theta);
y = lambda;

% DS Should be also in the config file!
       p00 =     -0.3611;  %(-0.3697, -0.3524)
       p10 =     0.01458;  %(0.01395, 0.01522)
       p01 =      0.2876;  %(0.284, 0.2912)
       p20 =  -0.0002757;  %(-0.0002912, -0.0002603)
       p11 =   -0.003615;  %(-0.003989, -0.003242)
       p02 =    -0.02608;  %(-0.02661, -0.02555)
       p21 =   9.747e-05;  %(8.936e-05, 0.0001056)
       p12 =   -0.001189;  %(-0.001264, -0.001114)
       p03 =    0.001228;  %(0.001195, 0.001261)
       p22 =  -2.193e-05;  %(-2.296e-05, -2.091e-05)
       p13 =   5.627e-05;  %(5.159e-05, 6.096e-05)
       p04 =  -2.211e-05;  %(-2.285e-05, -2.136e-05)


C_T = p00 + p10*x + p01*y + p20*x^2 + p11*x*y + p02*y^2 + p21*x^2*y +p12*x*y^2 + p03*y^3 + p22*x^2*y^2 + p13*x*y^3 + p04*y^4;

end
