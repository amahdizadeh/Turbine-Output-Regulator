%% rad2deg
% Function: converts from rad to degrees
%
% 
%% Usage:
% 
%  y = deg2rad(u)
% 
%% Input:
% 
% u: angle in rad
% 
%% Output:
% 
% y: angle in degrees
%
%% Modified:
%
%
%
%% ToDo:
%
% 
% 
%% Created:
% David Schlipf on          2009-01-11
%
% (c) Universitaet Stuttgart
% 

function y = deg2rad(u)
y = u * pi/180;
